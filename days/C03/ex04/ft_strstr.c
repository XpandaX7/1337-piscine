char	*ft_strstr(char *str, char *to_find)
{
	char	*s;
	char	*t;

	if (to_find[0] == '\0')
	{
		return (str);
	}
	while (*str)
	{
		s = str;
		t = to_find;
		while (*s && *t && *s == *t)
		{
			s++;
			t++;
		}
		if (*t == '\0')
		{
			return (str);
		}
		str++;
	}
	return (0);
}
